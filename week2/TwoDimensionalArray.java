

public class TwoDimensionalArray {
    public static void main(){
       String [][] clothColors = new String[2][3];
      //first row
       clothColors[0][0] = "Red";
       clothColors[0][1] = "blue";
       clothColors[0][2] = "green";

       //second row
       clothColors[1][0] = "orange";
       clothColors[1][1] = "yellow";
       clothColors[1][2] = "violet";
    }
}
